
public class Class3 {
	
	public static int multi(int n, int m) {
		return n * m;
	}

	public static void main(String[] args) {
		// static : 미리 준비된다는 의미. >> 실행 메모리에 올라가서 실행될 수 있도록 준비됨.
		//     		인스턴스를 생성하지 않고 실행을 시킬 때 static을 해준다.
		
		// 위의 함수의 경우 main에서 호출을 해야하기 때문에 static을 붙혀준다.
		
		
		System.out.println(123);

	}

}
